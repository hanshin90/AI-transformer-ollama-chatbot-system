from langchain_core.tools import tool

@tool
def calculator(expression: str) -> str:
    """
    [언제 사용?] 수학 계산이 필요할 때 사용한다.
    예: '12*(3+4)', '100/5'
    """
    try:
        return str(eval(expression))
    except Exception as e:
        return f"Error: {e}"

