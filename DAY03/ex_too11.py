# 중복되는 검색 Tool 2개를 “충돌 없이” 리팩토링
# ==================================================================
# ==================================================================
# 모듈 로딩
# ==================================================================
from langchain_core.tools import tool

# ==================================================================
# Tool 생성
# -> Tool 이름이 유사함
# -> docstring이 완전히 동일
# -> “언제 어느 Tool을 써야 하는지” 정보 없음
# ==================================================================
@tool
def search(query: str) -> str:
    """
    문서를 검색합니다.
    """
    return f"[search] query={query}"


@tool
def search_docs(query: str) -> str:
    """
    문서를 검색합니다.
    """
    return f"[search_docs] query={query}"

# ==================================================================
# Tool 개선
# -> Tool 이름을 목적 중심으로 변경
# -> docstring에 선택 기준을 명확히 작성
# -> 두 Tool이 서로 대체되지 않도록 역할 분리
# ==================================================================

@tool
def search_web(query: str) -> str:
    """
    [언제 사용?] 최신 정보나 외부 근거가 필요할 때 사용한다.
    뉴스, 웹사이트, 공개 자료 검색에 적합하다.
    """
    return f"[WEB SEARCH] query={query}"


@tool
def search_internal_docs(query: str) -> str:
    """
    [언제 사용?] 사내 문서나 내부 자료에서 정보를 찾을 때 사용한다.
    외부 정보 검색에는 사용하지 않는다.
    """
    return f"[INTERNAL DOC SEARCH] query={query}"